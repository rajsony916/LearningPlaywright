//Indentifiers are names given to variables, functions, classes, etc. They are used to identify and refer to these entities in the code. In JavaScript, there are certain rules for naming identifiers:

//1. An identifier must start with a letter, underscore (_), or dollar sign ($). It cannot start with a number.
//2. After the first character, an identifier can contain letters, digits, underscores, or dollar signs.
//3. Identifiers are case-sensitive. For example, "myVariable" and "myvariable" are considered different identifiers.
//4. Reserved keywords cannot be used as identifiers. For example, you cannot use "var", "function", "if", etc., as variable names.

//Literals are fixed values that are directly written in the code. They can be of various types, such as numbers, strings, booleans, etc. For example:
let numberLiteral = 42; // This is a number literal
let stringLiteral = "Hello, World!"; // This is a string literal
let booleanLiteral = true; // This is a boolean literal

//Operators are symbols that perform operations on operands. They can be arithmetic operators (+, -, *, /), assignment operators (=, +=, -=), comparison operators (==, ===, !=), logical operators (&&, ||), etc. For example:
let a = 10;
let b = 5;

//Keywords are reserved words that have a special meaning in JavaScript. They cannot be used as identifiers. Some examples of keywords include "var", "let", "const", "function", "if", "else", "for", "while", etc. These keywords are used to define the structure and behavior of the code. For example:
function greet(name) {
    console.log("Hello, " + name + "!");
}

